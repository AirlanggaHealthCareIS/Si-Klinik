    public boolean TambahPenyakit(){
        boolean isi1 = false;
        boolean sukses = false;
        
        if(!NAMA.getText().equals("")){
            System.out.println("isi1");
            isi1 = true;
        }
        
        if (!cekNamaPenyakitSudahAda()){
        if(isi1)    {
            
            String N = NAMA.getText();
            
            String ID = null;
            try {
                ID = "PENY"+(ss.getPenyakitAll().size()+1);
            } catch (RemoteException ex) {
                Logger.getLogger(Form_Data_Penyakit_Tambah.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
               Penyakit s= new Penyakit();
               s.setId_Penyakit(ID);
               s.setNama_Penyakit(N);
               
               if(ss.insertPenyakit(s)!=null){
                   sukses = true;
                    System.out.println("masuk");
                    int opsi = JOptionPane.showConfirmDialog(null, "Data Anda berhasil disimpan. Apakah Anda akan menambahkan data lagi?","", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);                    
                    if(opsi==0){
                        refresh();
                    }
                    else{
                       try {
                           if(gui!=null){
                               gui.updatePanel(new Form_data_penyakit(this.gui));
                               this.dispose();
                           }
                       } catch (NotBoundException ex) {
                           
                       }
                    }
                }
                
            }
            catch(RemoteException exception){
                exception.printStackTrace();
            }
            
        } 
        
        else{
            if(!isi1){
                NAMA.setBackground(Color.red);
            }
            
            JOptionPane.showMessageDialog(null, "Ada kesalahan pada kolom isian Anda. Mohon memperbaiki field yang berwarna merah untuk melanjutkan.", "ERROR", JOptionPane.ERROR_MESSAGE);
            
        }
        
        }
        else{
        JOptionPane.showMessageDialog(null, "Nama Penyakit sudah ada!", "ERROR", JOptionPane.ERROR_MESSAGE);
        
    }
        return sukses;
    }
    
    
